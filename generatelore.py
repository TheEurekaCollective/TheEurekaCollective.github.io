article = """===TYKUPIEL
Tykupiel is the capital of Zvrenda Holdings and the {Karanaan}’s proxy in the {Galactic Commonwealth}. It is a gigantic, multi-layered sphere that hosts a G-type star, with the outermost sphere being 200 million kilometers in diameter. While it currently resides within Aeternum, it has made trips to other Great Cities of the galaxy as well as to the Andromeda Galaxy.
==LAYERS & SYSTEMS
Tykupiel is composed of thousands of interconnected spheres and systems that each serve a distinct purpose.
=Host Star
The star that Tykupiel is built around is a G-type star approximately 1.1 times the size of Sol.
=Power Collection & Distribution
The power layers, at the direction of administrator AIs, distribute solar energy evenly between the many layers of Tykupiel, ensuring that the layers that need the energy the most have their needs fulfilled. These layers also play host to a {wormhole} matrix that syphons out power and sends it to the other stellar engines of the Karanaan.
=Gravitic Harness
The gravitic harness utilized by the inner layers keeps the host star’s position stable during transit, preventing the stellar object from crashing into the constructs built around it as well as nullifying its gravitic effects. Each layer of Tykupiel also uses similar harnesses for similar purposes.
=Computation
The computation layers host powerful AI that monitors and maintains Tykupiels extensive systems. These utilize microscopic wormholes to improve communication between the furthest parts of the computers, improving processing speeds and allowing the AI to react quickly to any situation.
=Industrial
The industrial layers of Tykupiel produce the drones and materials needed to sustain the engine’s operations. From transmutation arrays to organic synthesis chambers, the factories of Tykupiel provide for any of its inhabitants' needs.
=Residential
The residential layers consist of dispersed biodomes containing the atmospheric and geological conditions needed by the various races that compose Karanaan to survive. These layers are generally reserved for employees or long-term residents of Tykupiel.
=Royal
The royal layers of Tykupiel contain Olande’s multitude of thrones, hoards, and estates. Every day, Olande has a new, unique palace constructed for itself, where it hosts a grand party for its retinue and favored peoples. The Galactic elite clamor to gain access to these parties, but almost all are turned away. Even after constructing and abandoning each palace daily for several thousand years, the royal layers have not even been filled to a hundredth of a single percent.
=Commercial
The commercial sectors are where tourists and visitors of Tykupiel stay and relax. Here, the cultures of a thousand worlds are represented.
=Ports
Spread throughout the structure of Tykupiel are millions of access and entry points to facilitate the colossal amounts of people and goods that enter and exit the structure every day. It is said that if Tykupiel were to leave the Infinite City, the city’s gross domestic output would drop by nearly eighty-five percent.
=Gravitic Engine
The gargantuan gravitic engine utilized by Tykupiel allows for it to make correctional adjustments to its positioning as well as provides it the necessary thrust to move in both the Material Plane and the Void. Due to the sheer size of Tykupiel and its strong gravitational influence, its traversal of both Material and Void lanes is extremely restricted.
={Void} Drive
Tykupiel’s primary void drive and reality stabilization field allow Tykupiel to make jumps into the Void. Different sections of Tykupiel are also equipped with their own void drives and stabilization fields in the event of an emergency. To operate the primary void drive, a signal is sent by wormhole to the other stellar engines of the Karanaan. These engines, when receiving a jump order, reverse the polarity of their wormholes to push energy into Tykupiel, giving the construct a sufficient burst of energy to allow itself to void travel.
=Defense
Tykupiel, being a stellar engine, comes equipped with many different defensive and offensive measures. The shipyards of Tykupiel host a sizable defense fleet consisting of trillions of ships piloted by Olande’s personal guard. Energy shields cover every centimeter of the outer layer, with the majority being concentrated at the exit and entry points of the stellar engine. Automated defense batteries poke out of hyperelement armor hundreds of kilometers thick. Should the vessel or its inhabitants be in grave danger, the stellar engine will initiate an evasive voidjump. Finally, there stands Olande itself, an onturgist that can will away the existence of most attackers.
=Wormhole Array
To traverse the massive structure that is the wandering sun, a wormhole transportation grid has been devised. These are series of evenly spaced gates that open periodically to facilitate the movement of physical goods and personnel at faster-than-light speeds. It is not uncommon to see fleets of starships congregate in front of these gates before their opening.
=Centralized Pillars
The centralized pillars are the physical connections between the different layers of Tykupiel, containing the infrastructure needed to keep each layer operational. These are the centers of Tykupiel’s civilizations, with more than ninety-nine percent of the population living in the vicinity of one of these. Possessing everything from energy conductors to wormhole gates, the citizens of Tykupiel have dubbed these structures “Towers of Life”.
==POPULATION
While the expansive nature of Tykupiel makes it difficult to account for every individual that lives or dies within the structure, the monitor AIs estimate the current population to be at 1.03 quadrillion sophont life forms.
==HIDDEN WORLDS
Even with access to extensive surveillance systems and monitors, the AI can’t see all that exists within Tykupiel. Several portions of the structure have been sealed off or abandoned, leading researchers and explorers to theorize that hidden civilizations and life may have sprung up in these locations."""

# this is the file the html gets saved in, make sure to include
# the .html extension
filename = "tykupiel-lore.html"

# ignore this stuff

htmlcode = ""
lines = article.split('\n')
for line in lines:
	curID = line.lower().replace(' ', '-').replace('{', '').replace('}', '').replace('=', '').replace('_', '')
	for c in "{}=_":
		if c in curID:
			curID = curID.replace(c, '')
	if len(line)>=3 and line[:3]=='===':
		htmlcode += "\t\t<h1 id='"+curID+"'>" + line[3:] + "</h1>\n\t\t<span class='underline1'></span>\n"
	elif len(line)>=2 and line[:2]=='==':
		htmlcode += "\t\t<h2 id='"+curID+"'>" + line[2:] + "</h2>\n\t\t<span class='underline2'></span>\n"
	elif len(line)>=1 and line[:1]=='=':
		htmlcode += "\t\t<h3 id='"+curID+"'>" + line[1:] + "</h3>\n"
	elif len(line)>=1 and line[:1]=='_':
		htmlcode += "\t\t<h4 id='"+curID+"'>" + line[1:] + "</h4>\n"
	else:
		htmlcode += "\t\t<p class='infoparagraph'>" + line + "</p>\n"

htmlcode = htmlcode.replace('{', "<a class='infolink' href='(LINKGOESHERE)'>")
htmlcode = htmlcode.replace('}', "</a>")
for link in articlelinks:
	htmlcode = htmlcode.replace('(LINKGOESHERE)', link, 1)

s = open('loretemplate.txt', 'r').read()
s = s.replace('(PAGETITLE)', tabtitle)
s = s.replace('(INFOIMAGE)', infoimagesource)
s = s.replace('(KEYWORDS)', keywords)
for i in range(6):
	s = s.replace('(H'+str(i)+')', infoheadings[i])
	s = s.replace('(I'+str(i)+')', infoentries[i])
s = s.replace('(CONTENT)', htmlcode)

f = open(filename, 'w')
f.write(s)
f.close()
