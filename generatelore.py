article = """===TALAPHRIAN ORDER
The Talaphrian Order of Knights, otherwise known as the Knights Talaphria, are a warrior cabal originating from the planet Vitar. They are led by Eyon and possess incredible reality-bending powers. They are frequently hired to fulfill security contracts and they host many activities such as the Proving Grounds.
==ORIGIN
The Talaphrian Order’s name originates from the talaph tree, the crimson tree that breached the heavens. It is also the name of the talaph system, the method in which members of the Order gain their abilities.
==HISTORY
The Order has existed since the pre-war era, hailing all the way from when the Yichzoy Dynasty fell and a new age came into fruition on the planet of Vitar.
=Eyon
The origin story of the Talaphrian Order begins with Eyon, its founder. Eyon was a liege knight in service of the Yichzoy Dynasty for nearly 60 Terran Years before its fall. Eyon was part of a diplomatic mission to the Ice Cavers of Ruqaqo when the Vitarian Civil War occurred, which resulted in the loss and death of the Vitarian aristocracy, including the Yichzoy Dynasty. The liege knights, who were born to fight and die, were left disillusioned and without purpose. These warriors were thus reduced to treading the lands, their skills left to rust and dull. Eyon suffered the same fate, coming back from its mission only to find its homeland in ruins. It wandered the lands for a while, acting as an occasional mercenary. That was when it entered the land of Umdil and cast its warrior garb aside to become a spiritualist of the Zogue monks. Eyon meditated for six years, pondering the meaning of the cosmos. That was when an Ascendance event occurred, turning Eyon into a new onturgist. Eyon treaded the continents, collecting liege-knights and great warriors along its travels. This collective would come to eventually be known as the Talaphrian Order.
=Theomachy
When the Theomachy began, the Talaphrian Order answered the call. All of the Knights Talaphria raised their arms to fight Aunox. However, this war came at a great cost. Millions of knights, forged over hundreds of millions of years, were killed. By the end of the war, only a few dozen knights were left, and Eyon was greatly weakened. To regain its strength, it enter the Great Sleep. However, to monitor and command the Knights Talaphria in its absence, it cleaved a piece of itself and formed the piece into its avatar, known as the Sage.
==RANKS
=Commendatore
Eyon, Saint of Swords, Master of Blades, Bearer of the Ebony Twins, Great Hero: Eyon is the Commendatore of the Knights Talaphria. It is the highest rank and title within the warrior order, giving absolute control over to its bearer.
=Principals and Groundsmasters
While Groundsmasters oversee the Proving Grounds, the Principals oversee the Schools. They are of equal rank and authority. The leaders of these two groups are Prime Groundsmaster and Prime Principal. The current Prime Groundsmaster is Zaehaldyn and the current Prime Principal is Aten.
=Adept
Adepts are knights who have gone through all seven schools and thus have completed their basic training within the Talaphrian Order. At this point, adepts refine their skills without guidance from any schools. An individual with the rank of adept can take contracts and fight within the Greater Proving Grounds.
=Aspirant
The neophytes of the Talaphrian Order are known as aspirants. There are seven grades of aspirants with each grade corresponding to the number of schools an aspirant has passed through.
==COMPOSITION
The Talaphrian Order is divided into seven schools, the Proving Grounds, and an administrative arm known as the Sellswords Association.
=Sellswords Association
The Sellswords Association is the administrative body that generates and allots contracts. It handles all of the administrative and maintenance duties of the Talaphrian Order. Anything that is not directly related to fighting in the Talaphrian Order is the responsibility of the Association. This division is composed primarily of non-combatants such as remnant Vitarians, although some aspirants and adepts do choose to work at the Association.
=Proving Grounds
The Proving Grounds are a collection of controlled spaces where adepts and aspirants both train and fight. They can be divided into two subtypes
the Lesser Proving Grounds and the Greater Proving Grounds. The Lesser Proving Grounds correspond to the seven schools and serve as testing facilities while the Greater Proving Grounds vary in both purpose and number. Regardless of the nature of the Proving Grounds, their events are live streamed throughout the entire Galactic Commonwealth. The grounds serve as a means of advertising the combatants’ abilities and histories, with some gaining attention similar to icons and celebrities.
=Schools
There are seven Schools of the Knights Talaphria, with each school aligning with a different series of skills devised by Eyon. For an aspirant to ascend to the rank of adept, they must go through all seven schools. The fastest completion of all seven schools was two Terran decades, but it takes the majority of applicants hundreds, if not thousands, of years. The education system of these schools is referred to as the talaph system.
=Minor Orders
Members of the Knights Talaphria have formed factions known as minor orders based on their charge or specialization. Some of these minor orders include the Order of the Outlands, the Sisters of Purity, and the Abyssal Sentries.
==LOCATIONS
=Pillared City
The Pillared City of Talaphria is a city-state residing within the greater Aeternum area. It is a series of free-floating towers that each have their individual purposes and properties.
_Hall of Heroes
The Hall of Heroes is a large mausoleum dedicated to honoring the warriors of the order that had fallen during the Theomachy. It is a great tower filled with the bodies of the dead, with a crimson talaph tree dominating the middle."""

